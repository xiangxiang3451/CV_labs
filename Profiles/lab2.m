% 1. Чтение изображения
img = imread('photo.png');

% 2. Преобразование изображения в градации серого
grayImg = im2gray(img);

% 3. Выбор строки, где находится штрих-код
% Предполагаем, что мы будем рисовать контур по средней строке
row = round(size(grayImg, 1) / 2);
profile = grayImg(row, :);

% 4. Построение профиля
figure;
plot(profile);
title('Профиль изображения вдоль штрих-кода');
xlabel('Позиция пикселя');
ylabel('Интенсивность');
